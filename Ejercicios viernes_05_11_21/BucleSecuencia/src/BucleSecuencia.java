public class BucleSecuencia {
    public static void main(String[] args) {
        for (int i = 1; i <= 25; i += 4) {
            System.out.print(i + "  ");
        }
    }
}

// Traza del bucle:

// 1. Se inicializa la variable i con el valor 1.
// 2. Se comprueba que i sea menor o igual que 25.
// 3. Se incrementa i en 4.
// 4. Se imprime i.
// 5. Si i es menor o igual que 25, se vuelve al paso 3.
// 6. Si i no es menor o igual que 25, se sale del bucle.
